package main

import (
	"fmt"
	"net/http"
)

// This file demonstrates how to use the compiled GoX components
// After running: goxc build -o . TodoApp.gox
// This will use the generated TodoApp.go file

func main() {
	http.HandleFunc("/", handleHome)
	http.HandleFunc("/static/", handleStatic)

	fmt.Println("GoX Todo App Server")
	fmt.Println("Starting server on http://localhost:8080")

	err := http.ListenAndServe(":8080", nil)
	if err != nil {
		fmt.Printf("Server error: %v\n", err)
	}
}

